/*
TP4 - CICLOS WHILE - Ejercicio 03
Hacer un programa para mostrar por pantalla los números del 20 al 1 en orden
decreciente.
Importante: El programa no tiene ningún ingreso de datos.
*/


#include <iostream>
using namespace std;

int main(){
    int i;
    i=20;

while(i>=1){
    cout << i << endl;
    i-=1;
}

    return 0;

}

